#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*returned_str;
	size_t	i;
	size_t	len;

	len = ft_strlen(s);
	returned_str = malloc(len + 1);
	if (!returned_str)
		return (NULL);
	i = 0;
	while (i < len)
	{
		returned_str[i] = s[i];
		i++;
	}
	returned_str[i] = '\0';
	return (returned_str);
}
